class Longitud:
    @staticmethod
    def metro_a_kilometro(metro):
        return f"Conversão de Metros a Kilometros: {metro / 1000}"
    
    @staticmethod
    def kilometros_a_millas(kilometros):
        return f"Conversão Kilometros a Millas: {kilometros * 0.621371}"

    @staticmethod
    def millas_a_metros(millas):
        return (f"Conversão de Millas a Metros: {millas * 1609.34}")